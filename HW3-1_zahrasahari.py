#Homework 3 - 1 - part a

i , j , k = 3 , 5 , 7
if i < j:
    if i < k:
        i = j 
    else :
        i = k 
        
        
else:
    if i > k:
        j = i 
    else :
        i = k 
        
print("i =" , i, "j =" , j,"k =" , k)

#result : i = 5 j = 5 k = 7

